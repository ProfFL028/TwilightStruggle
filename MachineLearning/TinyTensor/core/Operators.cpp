#include "Operators.h"

double Add::calc(double a, double b) {
    return a + b;
}

double Sub::calc(double a, double b) {
    return a - b;
}

double Mul::calc(double a, double b) {
    return a * b;
}

double Div::calc(double a, double b) {
    return a / b;
}
